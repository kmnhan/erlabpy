    def _connect_controls(self) -> None:
        for widget in (
            self.nrows_spin,
            self.ncols_spin,
            self.width_spin,
            self.height_spin,
            self.dpi_spin,
        ):
            typing.cast("QtWidgets.QAbstractSpinBox", widget).editingFinished.connect(
                self._controls_changed
            )
        self.nrows_spin.valueChanged.connect(self._controls_changed)
        self.ncols_spin.valueChanged.connect(self._controls_changed)
        for widget in (self.width_mm_spin, self.height_mm_spin):
            typing.cast("QtWidgets.QAbstractSpinBox", widget).editingFinished.connect(
                self._millimeter_controls_changed
            )
        for widget in (self.width_ratios_edit, self.height_ratios_edit):
            widget.editingFinished.connect(self._controls_changed)
        self.layout_mode_combo.currentTextChanged.connect(self._layout_mode_changed)
        self.layout_combo.currentTextChanged.connect(self._controls_changed)
        self.sharex_combo.currentTextChanged.connect(self._controls_changed)
        self.sharey_combo.currentTextChanged.connect(self._controls_changed)

    @QtCore.Slot()
    @QtCore.Slot(int)
    @QtCore.Slot(str)
    @QtCore.Slot(object)
    def _controls_changed(self, _value: object | None = None) -> None:
        if self._syncing or self._released:
            return
        try:
            width_ratios = self._ratio_tuple(self.width_ratios_edit.text())
            height_ratios = self._ratio_tuple(self.height_ratios_edit.text())
            common_updates = {
                "figsize": (self.width_spin.value(), self.height_spin.value()),
                "dpi": self.dpi_spin.value(),
                "layout": self._layout_value(),
            }
            if self._setup.layout_mode == "gridspec":
                setup = FigureSubplotsState.model_validate(
                    {
                        **self._setup.model_dump(mode="python"),
                        **common_updates,
                    }
                )
                setup = _gridspec_update_grid_settings(
                    setup,
                    self._active_grid_id,
                    nrows=self.nrows_spin.value(),
                    ncols=self.ncols_spin.value(),
                    width_ratios=width_ratios,
                    height_ratios=height_ratios,
                )
            else:
                setup = FigureSubplotsState(
                    layout_mode="subplots",
                    nrows=self.nrows_spin.value(),
                    ncols=self.ncols_spin.value(),
                    figsize=(self.width_spin.value(), self.height_spin.value()),
                    dpi=self.dpi_spin.value(),
                    layout=self._layout_value(),
                    sharex=self._combo_bool_or_text(self.sharex_combo),
                    sharey=self._combo_bool_or_text(self.sharey_combo),
                    width_ratios=width_ratios,
                    height_ratios=height_ratios,
                    gridspec=self._setup.gridspec,
                )
        except ValueError:
            return
        if setup != self._setup:
            self.setup_requested.emit(setup)

    @QtCore.Slot()
    def _millimeter_controls_changed(self) -> None:
        if self._syncing or self._released:
            return
        self._syncing = True
        try:
            self.width_spin.setValue(self.width_mm_spin.value() / _MM_PER_INCH)
            self.height_spin.setValue(self.height_mm_spin.value() / _MM_PER_INCH)
        finally:
            self._syncing = False
        self._controls_changed()

    @QtCore.Slot(str)
    def _layout_mode_changed(self, mode: str) -> None:
        if self._syncing or self._released or mode == self._setup.layout_mode:
            return
        self.layout_mode_requested.emit(mode)

    @QtCore.Slot(int)
    def _region_kind_changed(self, _index: int) -> None:
        kind = typing.cast(
            'typing.Literal["axes", "grid"]',
            self.gridspec_region_kind_combo.currentData(),
        )
        self.gridspec_layout_widget.set_creation_kind(kind)
        grid = _gridspec_grid_by_id(self._setup, self._active_grid_id)
        if grid is not None:
            self._refresh_gridspec_status(grid)

    @QtCore.Slot(object, str)
    def _region_created(self, span_object: object, kind_text: str) -> None:
        span = typing.cast("FigureGridSpecSpanState", span_object)
        kind = typing.cast('typing.Literal["axes", "grid"]', kind_text)
        try:
            setup, region_id = _gridspec_add_region(
                self._setup, self._active_grid_id, span, kind
            )
        except ValueError as exc:
            self.set_validation(str(exc))
            return
        if region_id:
            self._request_setup(setup, selected_region_id=region_id)

    @QtCore.Slot(str, object)
    def _region_changed(self, region_id: str, span_object: object) -> None:
        span = typing.cast("FigureGridSpecSpanState", span_object)
        try:
            setup = _gridspec_update_region_span(
                self._setup, self._active_grid_id, region_id, span
            )
        except ValueError as exc:
            self.set_validation(str(exc))
            return
        self._request_setup(setup, selected_region_id=region_id)

    @QtCore.Slot(str, str)
    def _region_selected(self, region_id: str, _kind: str) -> None:
        self.gridspec_layout_widget.set_selected_region(region_id)
        self._refresh_gridspec_region_controls()

    @QtCore.Slot(str)
    def _open_grid(self, grid_id: str) -> None:
        if _gridspec_grid_by_id(self._setup, grid_id) is None:
            return
        self._active_grid_id = grid_id
        self._syncing = True
        try:
            self._sync_active_grid_controls()
        finally:
            self._syncing = False
        self._refresh_gridspec_editor()

    @QtCore.Slot()
    def _open_selected_grid(self) -> None:
        if region_id := self.gridspec_layout_widget.selected_region_id():
            self._open_grid(region_id)

    @QtCore.Slot()
    def _open_parent_grid(self) -> None:
        path = _gridspec_grid_path(self._setup, self._active_grid_id)
        if len(path) > 1:
            self._open_grid(path[-2].grid_id)

    @QtCore.Slot()
    def _delete_selected_region(self) -> None:
        region_id = self.gridspec_layout_widget.selected_region_id()
        if not region_id:
            return
        selected_region_id = _gridspec_nearest_axes_after_region_delete(
            self._setup, self._active_grid_id, region_id
        )
        setup = _gridspec_remove_region(self._setup, self._active_grid_id, region_id)
        self._request_setup(setup, selected_region_id=selected_region_id)

    @QtCore.Slot()
    def _region_label_changed(self) -> None:
        region_id = self.gridspec_layout_widget.selected_region_id()
        if not region_id:
            return
        name = self.gridspec_region_label_edit.text().strip()
        error = _gridspec_axis_variable_name_error(
            self._setup,
            region_id,
            name,
            reserved_names=self._reserved_names,
        )
        if error:
            self.set_validation(error, invalid_name=True)
            return
        self.gridspec_region_label_edit.setProperty("invalid", False)
        grid = _gridspec_grid_by_id(self._setup, self._active_grid_id)
        if grid is not None:
            self._refresh_gridspec_status(grid)
        setup = _gridspec_update_axis_variable_name(self._setup, region_id, name)
        self._request_setup(setup, selected_region_id=region_id)

    def _request_setup(
        self, setup: FigureSubplotsState, *, selected_region_id: str | None = None
    ) -> None:
        if setup == self._setup:
            return
        self._pending_selected_region_id = selected_region_id
        self.setup_requested.emit(setup)

    def _sync_active_grid_controls(self) -> None:
        setup = self._setup
        if setup.layout_mode == "gridspec":
            grid = _gridspec_grid_by_id(setup, self._active_grid_id)
            if grid is None:
                self._active_grid_id = setup.gridspec.root.grid_id
                grid = setup.gridspec.root
            self.nrows_spin.setValue(grid.nrows)
            self.ncols_spin.setValue(grid.ncols)
            self.width_ratios_edit.setText(_format_tuple(grid.width_ratios))
            self.height_ratios_edit.setText(_format_tuple(grid.height_ratios))
        else:
            self.nrows_spin.setValue(setup.nrows)
            self.ncols_spin.setValue(setup.ncols)
            self.width_ratios_edit.setText(_format_tuple(setup.width_ratios))
            self.height_ratios_edit.setText(_format_tuple(setup.height_ratios))
        gridspec_mode = setup.layout_mode == "gridspec"
        self.gridspec_editor_container.setVisible(gridspec_mode)
        self.gridspec_editor_widget.setVisible(gridspec_mode)
        self.sharex_combo.setEnabled(not gridspec_mode)
        self.sharey_combo.setEnabled(not gridspec_mode)

    def _sync_size_mm_controls(self) -> None:
        self.width_mm_spin.setValue(self._setup.figsize[0] * _MM_PER_INCH)
        self.height_mm_spin.setValue(self._setup.figsize[1] * _MM_PER_INCH)

    def _refresh_gridspec_editor(self) -> None:
        setup = self._setup
        if setup.layout_mode != "gridspec":
            self.gridspec_editor_widget.setVisible(False)
            return
        self.gridspec_editor_widget.setVisible(True)
        grid = _gridspec_grid_by_id(setup, self._active_grid_id)
        if grid is None:
            self._active_grid_id = setup.gridspec.root.grid_id
            grid = setup.gridspec.root
        grid_names = _gridspec_grid_display_names(setup)
        regions = [
            _GridSpecRegionInfo(
                axis.axes_id,
                "axes",
                axis.span,
                _gridspec_axis_display_name(
                    setup, axis.axes_id, reserved_names=self._reserved_names
                ),
                _gridspec_region_valid(grid, axis.span),
            )
            for axis in grid.axes
        ]
        regions.extend(
            _GridSpecRegionInfo(
                child.grid_id,
                "grid",
                child.span,
                grid_names.get(child.grid_id, child.grid_id),
                _gridspec_region_valid(grid, child.span),
            )
            for child in grid.child_grids
            if child.span is not None
        )
        self.gridspec_layout_widget.set_edit_grid(
            grid,
            regions,
            {
                axes_id: _gridspec_axis_display_name(
                    setup, axes_id, reserved_names=self._reserved_names
                )
                for axes_id in _gridspec_all_axes_ids(setup)
            },
        )
        self._refresh_gridspec_breadcrumbs()
        self._refresh_gridspec_region_controls()
        self._refresh_gridspec_status(grid)

    def _refresh_gridspec_status(self, grid: FigureGridSpecGridState) -> None:
        invalid_regions = [
            _gridspec_region_label(
                self._setup,
                grid,
                axis.axes_id,
                reserved_names=self._reserved_names,
            )
            for axis in grid.axes
            if not _gridspec_region_valid(grid, axis.span)
        ]
        invalid_regions.extend(
            _gridspec_region_label(
                self._setup,
                grid,
                child.grid_id,
                reserved_names=self._reserved_names,
            )
            for child in grid.child_grids
            if child.span is None or not _gridspec_region_valid(grid, child.span)
        )
        if invalid_regions:
            self.gridspec_status_label.setText(
                "Some regions are outside the active grid: "
                + ", ".join(invalid_regions)
                + ". Increase rows/columns or delete and redraw them."
            )
        elif _gridspec_has_invalid_regions(self._setup.gridspec.root):
            self.gridspec_status_label.setText(
                "A nested grid contains regions outside its bounds."
            )
        else:
            kind = self.gridspec_region_kind_combo.currentText()
            self.gridspec_status_label.setText(
                f"Drag cells to create {kind.lower()} regions. "
                "Double-click a nested grid to edit it."
            )

    def _refresh_gridspec_breadcrumbs(self) -> None:
        while self.gridspec_breadcrumb_layout.count():
            item = self.gridspec_breadcrumb_layout.takeAt(0)
            if item is None:
                continue
            if widget := item.widget():
                widget.deleteLater()
        self._breadcrumb_buttons.clear()
        path = _gridspec_grid_path(self._setup, self._active_grid_id)
        for index, grid in enumerate(path):
            if index:
                self.gridspec_breadcrumb_layout.addWidget(
                    QtWidgets.QLabel(">", self.gridspec_breadcrumb_widget)
                )
            button = QtWidgets.QToolButton(self.gridspec_breadcrumb_widget)
            button.setText(_gridspec_grid_display_name(self._setup, grid.grid_id))
            button.setToolTip("Open this GridSpec grid.")
            button.clicked.connect(
                lambda _checked=False, grid_id=grid.grid_id: self._open_grid(grid_id)
            )
            self._breadcrumb_buttons.append(button)
            self.gridspec_breadcrumb_layout.addWidget(button)
        self.gridspec_breadcrumb_layout.addStretch(1)
        self.gridspec_parent_grid_button.setEnabled(len(path) > 1)

    def _refresh_gridspec_region_controls(self) -> None:
        region_id = self.gridspec_layout_widget.selected_region_id()
        grid = _gridspec_grid_by_id(self._setup, self._active_grid_id)
        label = ""
        kind = ""
        placeholder = "Autogenerated"
        if grid is not None and region_id:
            code_names = _gridspec_axis_code_names(
                self._setup, reserved_names=self._reserved_names
            )
            for axis in grid.axes:
                if axis.axes_id == region_id:
                    label = axis.label
                    kind = "axes"
                    placeholder = (
                        f"Autogenerated: {code_names.get(region_id, region_id)}"
                    )
                    break
            if not kind and any(
                child.grid_id == region_id for child in grid.child_grids
            ):
                kind = "grid"
        with QtCore.QSignalBlocker(self.gridspec_region_label_edit):
            self.gridspec_region_label_edit.setText(label)
        has_region = bool(kind)
        self.gridspec_region_label_edit.setPlaceholderText(placeholder)
        self.gridspec_region_label_edit.setProperty("invalid", False)
        self.gridspec_region_name_label.setVisible(kind != "grid")
        self.gridspec_region_label_edit.setVisible(kind != "grid")
        self.gridspec_region_label_edit.setEnabled(kind == "axes")
        self.gridspec_delete_region_button.setEnabled(has_region)
        self.gridspec_open_grid_button.setEnabled(kind == "grid")
        self.gridspec_open_grid_button.setToolTip(
            "Edit the selected nested grid."
            if kind == "grid"
            else "Select a nested grid region to edit it."
        )
        self.gridspec_delete_region_button.setToolTip(
            "Delete the selected axes or nested grid region."
            if has_region
            else "Select an axes or nested grid region to delete it."
        )

    @staticmethod
    def _set_combo_value(combo: QtWidgets.QComboBox, value: str) -> None:
        combo.setCurrentIndex(max(combo.findText(value), 0))

    @staticmethod
    def _ratio_tuple(text: str) -> tuple[float, ...]:
        values = tuple(float(value) for value in _literal_sequence_from_text(text))
        if any(value <= 0.0 for value in values):
            raise ValueError("subplot ratios must be positive")
        return values

    def _layout_value(
        self,
    ) -> typing.Literal["constrained", "compressed", "tight", "none"] | None:
        text = self.layout_combo.currentText()
        if text == "default":
            return None
        return typing.cast(
            'typing.Literal["constrained", "compressed", "tight", "none"]',
            text,
        )

    @staticmethod
    def _combo_bool_or_text(
        combo: QtWidgets.QComboBox,
    ) -> bool | typing.Literal["none", "all", "row", "col"]:
        text = combo.currentText()
        if text == "True":
            return True
        if text == "False":
            return False
        return typing.cast('typing.Literal["none", "all", "row", "col"]', text)
