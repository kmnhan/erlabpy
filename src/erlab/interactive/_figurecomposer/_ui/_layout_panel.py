class FigureLayoutPanel(QtWidgets.QWidget):
    """Edit a figure setup while leaving document commits to the owner."""

    setup_requested = QtCore.Signal(object)
    layout_mode_requested = QtCore.Signal(str)

    def __init__(self, parent: QtWidgets.QWidget | None = None) -> None:
        super().__init__(parent)
        self.setObjectName("figureComposerLayoutPage")
        self._setup = FigureSubplotsState()
        self._reserved_names: tuple[str, ...] = ()
        self._active_grid_id = self._setup.gridspec.root.grid_id
        self._pending_selected_region_id: str | None = None
        self._breadcrumb_buttons: list[QtWidgets.QToolButton] = []
        self._syncing = False
        self._released = False
        self._build_ui()
        self.set_setup(self._setup)

    def set_setup(
        self,
        setup: FigureSubplotsState,
        *,
        reserved_names: Iterable[str] = (),
        selected_region_id: str | None = None,
    ) -> None:
        """Project one immutable setup snapshot into every layout control."""
        if self._released:
            return
        previous_selection = self.gridspec_layout_widget.selected_region_id()
        desired_selection = (
            selected_region_id
            if selected_region_id is not None
            else self._pending_selected_region_id
        )
        if desired_selection is None:
            desired_selection = previous_selection
        self._pending_selected_region_id = None
        self._setup = setup
        self._reserved_names = tuple(reserved_names)
        if (
            setup.layout_mode != "gridspec"
            or _gridspec_grid_by_id(setup, self._active_grid_id) is None
        ):
            self._active_grid_id = setup.gridspec.root.grid_id

        self._syncing = True
        try:
            self._set_combo_value(self.layout_mode_combo, setup.layout_mode)
            self._sync_active_grid_controls()
            self.width_spin.setValue(setup.figsize[0])
            self.height_spin.setValue(setup.figsize[1])
            self._sync_size_mm_controls()
            self.dpi_spin.setValue(setup.dpi)
            self._set_combo_value(self.layout_combo, setup.layout or "default")
            self._set_combo_value(self.sharex_combo, str(setup.sharex))
            self._set_combo_value(self.sharey_combo, str(setup.sharey))
            self._refresh_gridspec_editor()
            self.gridspec_layout_widget.set_selected_region(desired_selection)
            self._refresh_gridspec_region_controls()
        finally:
            self._syncing = False

    def set_reserved_names(self, reserved_names: Iterable[str]) -> None:
        """Update names unavailable to generated axes variables.

        Source edits do not change the figure setup, but they can change both the
        displayed autogenerated axes names and validation of the selected axes name.
        Refresh those projections without emitting a layout edit.
        """
        names = tuple(reserved_names)
        if self._released or names == self._reserved_names:
            return
        self._reserved_names = names
        if self._setup.layout_mode == "gridspec":
            self._refresh_gridspec_editor()

    def set_validation(self, text: str, *, invalid_name: bool = False) -> None:
        """Present a rejected layout edit without changing document state."""
        self.gridspec_region_label_edit.setProperty("invalid", invalid_name)
        self.gridspec_status_label.setText(text)

    def release(self) -> None:
        """Detach application-wide event filters before owner teardown."""
        if self._released:
            return
        self._released = True
        self.gridspec_layout_widget.release()

    def closeEvent(self, event: QtGui.QCloseEvent | None) -> None:
        self.release()
        if event is not None:
            super().closeEvent(event)

